using GGUF, Test

modelbuf = [
    0x47, 0x47, 0x55, 0x46, # GGUF
    0x01, 0x00, 0x00, 0x00, # version
    0x01, 0x00, 0x00, 0x00, # tensorn (uint64)
    0x00, 0x00, 0x00, 0x00,
    0x01, 0x00, 0x00, 0x00, # metadatan (uint64)
    0x00, 0x00, 0x00, 0x00,
]

@testset "header" begin
    @test magic(modelbuf) == 0x46554747
    @test version(modelbuf) == 1
    @test tensorn(modelbuf) == 1
    @test metadatan(modelbuf) == 1
end